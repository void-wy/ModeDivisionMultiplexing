#include "CCD.H"





CCD::~CCD()
{
	finalizeICC();

	destroyWindow();

	releaseImage();
}



void CCD::finalizeICC()
{
	delete []pImageBuffer;

	status = EndCGCard(hcg);

	CG_VERIFY(status);
}



void CCD::destroyWindow()
{
	cvWaitKey(0);

	cvDestroyWindow("Image_CCD");
}



void CCD::releaseImage()
{
	cvReleaseImage(&imageCCD);
}

