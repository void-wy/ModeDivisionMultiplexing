#include "SimulatedAnnealing.h"





SimulatedAnnealing::~SimulatedAnnealing()
{
	destroyWindow();

	releaseImage();
}



void SimulatedAnnealing::destroyWindow()
{
	cvWaitKey(0);

	cvDestroyWindow("Image_Ideal");
}



void SimulatedAnnealing::releaseImage()
{
	cvReleaseImage(&imageIdeal);
}

