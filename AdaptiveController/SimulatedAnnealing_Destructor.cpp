#include "SimulatedAnnealing.h"





SimulatedAnnealing::~SimulatedAnnealing()
{
	finalizeCCD();

	closeFileLog();
}



void SimulatedAnnealing::finalizeCCD()
{
	ccd->releaseImageCopy(imageCCD);
}



void SimulatedAnnealing::closeFileLog()
{
	fileLog->close();

	delete fileLog;
}

