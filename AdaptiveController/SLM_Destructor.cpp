//关闭SLM
SLM::~SLM()
{
	destroyWindow();	//销毁SLM相关窗口

	releaseImage();	//释放SLM相关图像资源
}

//销毁SLM相关窗口
void SLM::destroyWindow()
{
	cvWaitKey(0);

	cvDestroyWindow("Image_SLM");
	cvDestroyWindow("Image_Visible");
}

//释放SLM相关图像资源
void SLM::releaseImage()
{
	cvReleaseImage(&imageSLM);
	cvReleaseImage(&imageVisible);
}

