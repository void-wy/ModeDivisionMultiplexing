#include "SLM.H"





SLM::~SLM()
{
	destroyWindowSLM();

	releaseImage();

	closeFileLog();
}



void SLM::destroyWindowSLM()
{
	cvDestroyWindow("Image_SLM");
}



void SLM::releaseImage()
{
	cvReleaseImage(&imageSLM);
	cvReleaseImage(&imageVisible);
	cvReleaseImage(&imageDesired);
}



void SLM::closeFileLog()
{
	fileLog->close();

	delete fileLog;
}

