#include "FakeCCD.h"





FakeCCD::~FakeCCD()
{
	destroyWindow();

	releaseImage();
}



void FakeCCD::destroyWindow()
{
	cvWaitKey(0);

	cvDestroyWindow("Image_CCD");
}



void FakeCCD::releaseImage()
{
	cvReleaseImage(&imageCCD);
	cvReleaseImage(&imageDesired);
}

