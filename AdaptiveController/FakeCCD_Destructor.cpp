#include "FakeCCD.h"





FakeCCD::~FakeCCD()
{
	releaseImage();

	closeFileLog();
}



void FakeCCD::releaseImage()
{
	cvReleaseImage(&imageCCD);
	cvReleaseImage(&imageDesired);
}



void FakeCCD::closeFileLog()
{
	fileLog->close();

	delete fileLog;
}

