#include "RealCCD.h"





RealCCD::~RealCCD()
{
	finalizeICC();

	releaseImage();

	closeFileLog();
}



void RealCCD::finalizeICC()
{
	delete []pImageBuffer;

	status = EndCGCard(hcg);

	CG_VERIFY(status);
}



void RealCCD::releaseImage()
{
	cvReleaseImage(&imageCCD);
	cvReleaseImage(&imageDesired);
}



void RealCCD::closeFileLog()
{
	fileLog->close();

	delete fileLog;
}

