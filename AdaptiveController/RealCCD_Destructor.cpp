#include "RealCCD.h"





RealCCD::~RealCCD()
{
	finalizeICC();

	destroyWindow();

	releaseImage();
}



void RealCCD::finalizeICC()
{
	delete []pImageBuffer;

	status = EndCGCard(hcg);

	CG_VERIFY(status);
}



void RealCCD::destroyWindow()
{
	cvWaitKey(0);

	cvDestroyWindow("Image_CCD");
}



void RealCCD::releaseImage()
{
	cvReleaseImage(&imageCCD);
	cvReleaseImage(&imageDesired);
}

